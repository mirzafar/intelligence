class StrUtils:
    @staticmethod
    def to_str(value):
        return str(value) if value is not None else ""


class ListUtils:
    @staticmethod
    def to_list_of_strs(value):
        if value is None:
            return []
        if isinstance(value, list):
            return [str(item) for item in value]
        return [str(value)]


async def save_file(file, _type: str = 'text') -> tuple[bool, Optional[str]]:
    if not file:
        return False, None

    file_path = settings.get('root_dir', '') + '/static/uploads'
    uid = str(uuid.uuid4())

    if _type == 'text':
        file_name = f'{file_path}/{uid[:2]}/{uid[2:4]}/{uid}.docx'
        os.makedirs(f'{file_path}/{uid[:2]}/{uid[2:4]}', 0o755, True)

        doc = Document()

        doc.add_paragraph(file)

        doc.save(file_name)

        return True, f'{uid[:2]}/{uid[2:4]}/{uid}.docx'

    else:
        ext = file.name.split('.')[len(file.name.split('.')) - 1]
        file_name = f'{file_path}/{uid[:2]}/{uid[2:4]}/{uid}.{ext}'
        os.makedirs(f'{file_path}/{uid[:2]}/{uid[2:4]}', 0o755, True)

        async with aiofiles.open(file_name, 'wb') as f:
            await f.write(file.body)

        return True, f'{uid[:2]}/{uid[2:4]}/{uid}.{ext}'


